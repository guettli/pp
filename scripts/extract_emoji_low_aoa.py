def main():
    parser = argparse.ArgumentParser(
        description="Extract emojis with low AoA for language learning"
    )
    parser.add_argument(
        "--max-aoa",
        type=float,
        default=12.0,
        help="Maximum Age of Acquisition threshold (default: 12 years)"
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("."),
        help="Output directory for generated files (default: current directory)"
    )

    args = parser.parse_args()

    print("=" * 70)
    print("Emoji AoA Extraction Tool")
    print("=" * 70)
    print(f"Maximum AoA threshold: {args.max_aoa} years")
    print(f"Output directory: {args.output_dir}")
    print()

    # Initialize analyzer
    print("Initializing phrase difficulty analyzer...")
    analyzer = PhraseDifficultyAnalyzer()
    print()

    # Process emojis
    english_words = set()
    german_words = set()
    emoji_results = []

    total_emojis = len(EMOJI_WORD_MAP)
    print(f"Processing {total_emojis} emojis...")
    print()

    for i, (emoji, english_word) in enumerate(EMOJI_WORD_MAP.items(), 1):
        if i % 50 == 0:
            print(f"  Progress: {i}/{total_emojis} emojis processed...")

        # Check AoA for English word
        aoa = analyzer.get_aoa(english_word, use_adult_fallback=False)

        if aoa is not None and aoa < args.max_aoa:
            english_words.add(english_word)
            emoji_results.append({
                "emoji": emoji,
                "english": english_word,
                "aoa": aoa
            })

            # Get German translation if available
            if english_word in GERMAN_ARTICLES:
                german_word, article = GERMAN_ARTICLES[english_word]
                german_words.add(german_word)
                german_words.add(f"{article.capitalize()} {german_word}")

    print()
    print("=" * 70)
    print(f"Found {len(emoji_results)} emojis with AoA < {args.max_aoa} years")
    print(f"  English words: {len(english_words)}")
    print(f"  German words: {len(german_words)}")
    print("=" * 70)
    print()

    # Sort results by AoA
    emoji_results.sort(key=lambda x: x["aoa"])

    # Write English words
    en_file = args.output_dir / "new-en.txt"
    with open(en_file, "w", encoding="utf-8") as f:
        for word in sorted(english_words):
            f.write(f"{word}\n")
    print(f"âœ… Wrote {len(english_words)} English words to {en_file}")

    # Write German words
    de_file = args.output_dir / "new-de.txt"
    with open(de_file, "w", encoding="utf-8") as f:
        for word in sorted(german_words):
            f.write(f"{word}\n")
    print(f"âœ… Wrote {len(german_words)} German words to {de_file}")

    # Write detailed report
    report_file = args.output_dir / "emoji-aoa-report.txt"
    with open(report_file, "w", encoding="utf-8") as f:
        f.write("=" * 70 + "\n")
        f.write(f"Emoji AoA Report (threshold: {args.max_aoa} years)\n")
        f.write("=" * 70 + "\n\n")

        for result in emoji_results:
            emoji = result["emoji"]
            english = result["english"]
            aoa = result["aoa"]

            f.write(f"{emoji}  {english:20s}  AoA: {aoa:4.1f} years\n")

            # Add German if available
